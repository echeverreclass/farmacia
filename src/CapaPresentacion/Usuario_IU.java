    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        try {
            if (txtDni.getText().length() > 0) {
                if (txtNombres.getText().length() > 0) {
                    if (txtApellidos.getText().length() > 0) {
                        if (txtDireccion.getText().length() > 0) {
                            if (txtCelular.getText().length() > 0) {
                                if (txtClave.getText().length() > 0) {

                                    // ATRIBUTOS
                                    Usuario oUsuario = new Usuario();
                                    //METODOS
                                    UsuarioBD oUsuarioBD = new UsuarioBD();
                                    oUsuario.setuDni(txtDni.getText().toUpperCase());
                                    oUsuario.setuNombre(txtNombres.getText().toUpperCase());
                                    oUsuario.setuApellidos(txtApellidos.getText().toUpperCase());
                                    oUsuario.setuDireccion(txtDireccion.getText().toUpperCase());
                                    oUsuario.setuCelular(txtCelular.getText().toUpperCase());
                                    oUsuario.setuTipo(Integer.parseInt(txtId_tipo_usuario.getText()));
                                    oUsuario.setuClave(txtClave.getText().toUpperCase());
                                    oUsuario.setTienda(cmbTienda.getSelectedItem().toString());
                                    boolean rpta = oUsuarioBD.registrarUsuario(oUsuario);
                                    if (rpta) {
                                        exito("Se registro con exito");
                                        reportar();
                                        limpiar();
                                    } else {
                                        JOptionPane.showMessageDialog(this, "Tiene problemas al registrar");
                                    }
                                } else {
                                    JOptionPane.showMessageDialog(this, "Ingrese clave");
                                    txtClave.requestFocus();
                                }
                            } else {
                                JOptionPane.showMessageDialog(this, "Ingrese celular");
                                txtCelular.requestFocus();
                            }
                        } else {
                            JOptionPane.showMessageDialog(this, "Ingrese direccion");
                            txtDireccion.requestFocus();
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "Ingrese apellidos");
                        txtApellidos.requestFocus();
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Ingrese nombre");
                    txtNombres.requestFocus();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Ingrese dni");
                txtDni.requestFocus();
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error");
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

