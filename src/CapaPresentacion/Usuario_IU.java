    private void tabla_reporte_usuariosMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabla_reporte_usuariosMousePressed
        // TODO add your handling code here:
        if (evt.getClickCount() == 2) {
            int fila = tabla_reporte_usuarios.getSelectedRow();
            txtDni.setText(tabla_reporte_usuarios.getValueAt(fila, 0).toString());
            txtNombres.setText(tabla_reporte_usuarios.getValueAt(fila, 1).toString());
            txtApellidos.setText(tabla_reporte_usuarios.getValueAt(fila, 2).toString());
            txtDireccion.setText(tabla_reporte_usuarios.getValueAt(fila, 3).toString());
            txtCelular.setText(tabla_reporte_usuarios.getValueAt(fila, 5).toString());
            cmbTipoUsuario.setSelectedItem(tabla_reporte_usuarios.getValueAt(fila, 6).toString());
            txtClave.setText(tabla_reporte_usuarios.getValueAt(fila, 4).toString());
            cmbTienda.setSelectedItem(tabla_reporte_usuarios.getValueAt(fila, 7).toString());
            txtDni.requestFocus();
            btnEliminar.setEnabled(true);
            btnModificar.setEnabled(true);
            btnRegistrar.setEnabled(false);
        }
    }//GEN-LAST:event_tabla_reporte_usuariosMousePressed

