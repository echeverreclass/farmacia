    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        try {
             if (txtDni.getText().length() > 0) {
                int aviso = JOptionPane.showConfirmDialog(rootPane, "Estas seguro de eliminar?", "MENSAJE", JOptionPane.YES_OPTION);
                if (aviso == 0) {
                    UsuarioBD oUsuarioBD = new UsuarioBD();
                    String dni= txtDni.getText();
                    boolean rpt = oUsuarioBD.eliminarUsuario(dni);
                    if (rpt) {
                        exito("Se elimino correctamente");
                        reportar();
                        limpiar();
                        txtDni.requestFocus();
                        btnRegistrar.setEnabled(true);
                    } else {
                        error("Hay problemas");
                    }
                }else{
                    error("Ingrese un dni para eliminar");
                    txtDni.requestFocus();
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
    }//GEN-LAST:event_btnEliminarActionPerformed

